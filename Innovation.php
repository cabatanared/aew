        <h3>Atomic Energy Week 2021 to brandish the latest in PH nuclear S&T innovations </h3>

        <p align="justify">
          <br>
          This year’s celebration of the Atomic Energy Week (AEW) will showcase the latest developments in nuclear science and technology, particularly local researches and technologies that address the current needs in the society, especially in the new normal condition. Now on its 49th year, the AEW will be held December 6-10 to be led by the Department of Science and Technology-Philippine Nuclear Research Institute (DOST-PNRI). <br><br>

With the theme “Agham at Teknolohiyang Pangnukleyar: Tugon sa Hamon ng Panahon”, the annual event will mostly be held online for everyone’s safety as the country transitions to a more relaxed quarantine classification.<br>
<br>
Among the most-awaited events in the annual celebration are the nuclear video-making contest, poster-making contest, technology session, virtual tours, and a youth summit. A gamified exhibit is also in the offing to give AEW guests a unique experience in engaging with nuclear scientists and their works. <br>
<br>
Among the event’s featured personalities and guests are Filipino scientists, professionals, and policy makers who will join talks and features on the latest nuclear and radiation-related innovations as well as the growing role of nuclear science in the country’s larger development policy. <br><br>

The AEW 2021 will be held in an easy-to-navigate platform that can be accessed at <a href="http://aew.pnri.dost.gov.ph">http://aew.pnri.dost.gov.ph</a> on the opening day. The five-day event will be simulcast across various social media platforms to reach more attendees while ensuring everyone’s safety. <br><br>

The annual AEW celebration, as mandated under Presidential Proclamation No. 1211 in 1973, aims to generate awareness of the Filipino people on the beneficial uses of nuclear science and technology. <br><br>

For more information, visit the DOST-PNRI Facebook page at <a href="https://www.facebook.com/PNRIDOST/" target="_blank">https://www.facebook.com/PNRIDOST/</a> or please contact the Institute at <a href="information@pnri.dost.gov.ph" target="_blank"> information@pnri.dost.gov.ph</a>.<br><br>
  
