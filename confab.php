        <h3>Research confab features 73 local & int’l nuclear studies</h3>

        <p align="justify">
          Presenting their latest innovations, some 73 researches in the field of nuclear science will be on spotlight at the 2nd Philippine Nuclear Research and Development Conference (PNRDC 2020) to be hosted by the Department of Science and Technology-Philippine Nuclear Research Institute (DOST-PNRI) from December 8 to 10, 2020.<br><br>

With the theme “AmBisyong Nukleyar Tungo sa Maunlad na Pilipinas,” the PNRDC highlights the growing role of the atom in national development, such as the diverse applications of nuclear energy that respond to society’s pressing needs and the possible inclusion of nuclear power in the country’s energy mix.<br><br>

“Through the PNRDC, we aim to disseminate the latest advancements of national and international trends in nuclear R&D, impacts, and policies to the public,” says Dr. Angel Bautista VII, PNRDC chairperson. “The PNRDC will likewise provide a forum for nuclear S&T and policy discourse among researchers and the general public.”<br><br>

Dr. Bautista said that as of this writing, the PNRDC 2020 already has on its list of participants some 237 authors with 53 affiliations from eight countries (Philippines, Korea, Thailand, Australia, Germany, Myanmar, Japan, and the USA).<br><br>

On the opening day, the conference will host a plenary session featuring the Nuclear Power Program of the Philippines with speakers from the DOST-PNRI, Department of Energy, the Philippine Senate, and the International Atomic Energy Agency IAEA). In the afternoon, an exciting career talk will have speakers from the Chulalongkorn University in Thailand, Korea Advanced Institute of Science and Technology, IAEA, and the DOST-PNRI.<br><br>

The next two days will feature oral and poster presentations of various researchers, with topics ranging from nuclear, radiation, and isotope studies on food, agriculture, health, education, policy, environment, theoretical and computational studies, nuclear engineering, radiation protection, nuclear waste management, radiation processing, and industry.<br><br>
